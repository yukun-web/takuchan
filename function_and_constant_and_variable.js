// 定数・変数
const nameOfFirstPerson = 'John'
let ageOfFirstPerson = 20

// 関数
function ageUpOfFirstPerson () {
  ageOfFirstPerson = ageOfFirstPerson + 1
}

// 呼び出し
console.log(nameOfFirstPerson) // John
console.log(ageOfFirstPerson) // 20
ageUpOfFirstPerson()
console.log(ageOfFirstPerson) // 21

